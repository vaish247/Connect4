package connect4main;

/**
 * @author ajith
 *
 */
public class Size {
  private int width;
  private int height;

  /**
   * @param width
   * @param height
   */
  public Size(int width, int height) {
      this.width = width;
      this.height = height;
  }

  /**
   * @return width
   */
  public int getWidth() {
      return width;
  }

  /**
   * @return height
   */
  public int getHeight() {
      return height;
  }

}
